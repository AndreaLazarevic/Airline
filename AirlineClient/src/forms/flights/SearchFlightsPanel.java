    public void populateFlightsTable() {
        List<Flight> flights = Controller.getInstance().getAllFlights(txtSearch.getText().trim());
        flights.sort((left, right) -> left.getFlightID()- right.getFlightID());
        ftm = new FlightsTableModel(flights);
        tblFlights.setModel(ftm);
        setJTableColumnsWidth(tblFlights, 960, 8, 15, 15, 10, 26, 26);
        tblFlights.setAutoCreateRowSorter(true);
    }

    
