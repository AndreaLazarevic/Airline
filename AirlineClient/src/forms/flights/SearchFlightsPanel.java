    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tblFlights.getSelectedRow();
        FlightsTableModel ftm = (FlightsTableModel) tblFlights.getModel();
        
        if (row != -1) {
            Flight f = ftm.getFlight(row);
            FrmFlight flightForm = new FrmFlight(cmf, true, FrmMode.EDIT);
            flightForm.populateForm(f, this, FrmMode.EDIT);
            flightForm.setVisible(true);
            populateFlightsTable();

        } else {
            JOptionPane.showMessageDialog(this, "You haven't selected any row! Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnEditActionPerformed

