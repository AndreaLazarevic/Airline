    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblFlights.getSelectedRow();
        
        if (row != -1) {
            Flight flight = ftm.getFlight(row);
                
            int user_response = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete flight " + flight + "? This will also delete reservations for this flight.", "Delete flight", JOptionPane.YES_NO_OPTION);
            if (user_response == JOptionPane.OK_OPTION) {
                Response response = Controller.getInstance().deleteFlight(flight);

                if (response.isSuccessful()) {
                    JOptionPane.showMessageDialog(this, response.getMessage(), "Successful.", JOptionPane.INFORMATION_MESSAGE);
                    populateFlightsTable();
                } else {
                    JOptionPane.showMessageDialog(this, response.getMessage(), "Unsuccessful.", JOptionPane.ERROR_MESSAGE);
                }
            }
        }  else {
            JOptionPane.showMessageDialog(this, "Please select the row which you want to delete.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


