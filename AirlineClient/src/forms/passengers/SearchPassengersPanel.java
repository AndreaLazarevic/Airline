    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        // TODO add your handling code here:
        int row = tblPassengers.getSelectedRow();
        PassengersTableModel ptm = (PassengersTableModel) tblPassengers.getModel();
        
        if (row != -1) {
            Passenger p = ptm.getPassenger(row);
            FrmPassenger passengerForm = new FrmPassenger(cmf, true, FrmMode.VIEW);
            passengerForm.populateForm(p, this, FrmMode.VIEW);
            passengerForm.setVisible(true);
            populatePassengersTable();

        } else {
            JOptionPane.showMessageDialog(this, "You haven't selected any row! Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_btnDetailsActionPerformed

