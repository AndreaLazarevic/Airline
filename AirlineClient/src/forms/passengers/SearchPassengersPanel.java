    private void populatePassengersTable() {
        
        List<Passenger> passengers = Controller.getInstance().getAllPassengers(txtSearch.getText().trim());
        passengers.sort((left, right) -> Integer.parseInt(left.getPassportNo())- Integer.parseInt(right.getPassportNo()));
        ptm = new PassengersTableModel(passengers);
        tblPassengers.setModel(ptm);
        setJTableColumnsWidth(tblPassengers, 720, 15, 30, 30, 25);
        tblPassengers.setAutoCreateRowSorter(true);

    }
    
   

   

