    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblReservations.getSelectedRow();
        
        if (row != -1) {
            Reservation reservation = rtm.getReservation(row);

            int points = r.getPassenger().getCollectedPoints()- r.getFlight().getPoints();
            r.getPassenger().setCollectedPoints(points);

            int currentTakenSeats = r.getFlight().getTakenSeats();
            r.getFlight().setTakenSeats(currentTakenSeats - 1);

            int user_response = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete reservation " + reservation + "?" + "If you delete this reservation, passenger will lose these points.", "Delete reservation", JOptionPane.YES_NO_OPTION);
            if (user_response == JOptionPane.OK_OPTION) {
                Response response = Controller.getInstance().deleteReservation(reservation);

                if (response.isSuccessful()) {
                    JOptionPane.showMessageDialog(this, response.getMessage(), "Successful.", JOptionPane.INFORMATION_MESSAGE);
                    populateReservationsTable();
                } else {
                    JOptionPane.showMessageDialog(this, response.getMessage(), "Unsuccessful.", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select the row which you want to delete.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

