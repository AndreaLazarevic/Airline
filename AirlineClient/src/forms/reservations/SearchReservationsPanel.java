    public void populateReservationsTable() {
        List<Reservation> list = Controller.getInstance().getAllReservations(txtSearch.getText());
        list.sort((left, right) -> left.getReservationID()- right.getReservationID());
        rtm = new ReservationsTableModel(list);
        tblReservations.setModel(rtm);
        setJTableColumnsWidth(tblReservations, 960, 7, 17, 53, 7, 16);
        tblReservations.setAutoCreateRowSorter(true);
    }
