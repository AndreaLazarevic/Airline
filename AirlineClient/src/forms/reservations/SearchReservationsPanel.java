    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        int row = tblReservations.getSelectedRow();
        ReservationsTableModel rtm = (ReservationsTableModel) tblReservations.getModel();

        if (row != -1) {
            Reservation r = rtm.getReservation(row);
            FrmReservation reservationForm = new FrmReservation(cmf, true, FrmMode.VIEW);
            reservationForm.populateForm(r, this, FrmMode.VIEW);
            reservationForm.setVisible(true);
            populateReservationsTable();

        } else {
            JOptionPane.showMessageDialog(this, "You haven't selected any row! Please try again.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDetailsActionPerformed

