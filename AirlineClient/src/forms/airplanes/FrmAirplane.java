    private void tblAirplaneClassesMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAirplaneClassesMouseReleased
        int[] rows = tblAirplaneClasses.getSelectedRows();

        List<AirplaneClass> list = actm.getSelectedAirplaneClasses(rows);
        int max = 0;
        
        for (AirplaneClass airplaneClass : list) {
            if(airplaneClass.getLastSeat() > max)
                max = airplaneClass.getLastSeat();
        }
        
        txtCapacity.setText(max + "");
        
    }//GEN-LAST:event_tblAirplaneClassesMouseReleased

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Airplane a = validation();
        if (a == null) {
            return;
        }

        Response response = Controller.getInstance().saveAirplane(a);

        if (response.isSuccessful()) {
            JOptionPane.showMessageDialog(this, response.getMessage(), "Successful", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, response.getMessage(), "Unsuccessful", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

