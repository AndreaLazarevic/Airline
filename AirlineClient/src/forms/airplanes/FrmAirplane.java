    private void populateAirplaneClassesTable() {
        List<AirplaneClass> list = Controller.getInstance().getAllAirplaneClasses();
        actm = new AirplaneClassesTableModel(list);
        tblAirplaneClasses.setModel(actm);
    }

    private Airplane validation() {
        boolean valid = FormValidator.getInstance().validateEmpty(txtCapacity, txtType);
        if (!valid) {
            JOptionPane.showMessageDialog(this, FormValidator.getInstance().getValidationMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        List<AirplaneClass> list = new LinkedList<AirplaneClass>();
        int[] rows = tblAirplaneClasses.getSelectedRows();
        
        if (rows.length != 0) {
            list = actm.getSelectedAirplaneClasses(rows);
        } else {
            JOptionPane.showMessageDialog(this, "Please select airplane classes.", "Error", JOptionPane.ERROR_MESSAGE);
            return null;
        }

        
        Airplane a = new Airplane(-1, txtType.getText(), Integer.parseInt(txtCapacity.getText()), list);
        return a;
    }

    
